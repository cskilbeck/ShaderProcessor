#pragma once

//////////////////////////////////////////////////////////////////////

struct Resource
{
	Resource();
	Resource(void *data, size_t size)
	{
		Data() = data;
		Size() = size;
	}

	virtual ~Resource();

	//////////////////////////////////////////////////////////////////////

	bool IsValid() const
	{
		return data != null;
	}

	//////////////////////////////////////////////////////////////////////

	operator void const *() const
	{
		return data;
	}

	//////////////////////////////////////////////////////////////////////

	void * &Data()
	{
		return data;
	}

	//////////////////////////////////////////////////////////////////////

	size_t &Size()
	{
		return size;
	}

	//////////////////////////////////////////////////////////////////////

protected:

	void *		data;
	size_t		size;
};

//////////////////////////////////////////////////////////////////////

struct FileResource: Resource
{
	FileResource(tchar const *name);
	~FileResource();
};

//////////////////////////////////////////////////////////////////////

struct WinResource: Resource
{
	WinResource(DWORD resourceID);
	~WinResource();
};
