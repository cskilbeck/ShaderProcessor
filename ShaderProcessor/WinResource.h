#pragma once

//////////////////////////////////////////////////////////////////////

struct Resource
{
	Resource();
	virtual ~Resource();

	//////////////////////////////////////////////////////////////////////

	bool IsValid() const
	{
		return data != null;
	}

	//////////////////////////////////////////////////////////////////////

	operator void const *() const
	{
		return data;
	}

	//////////////////////////////////////////////////////////////////////

	size_t Size() const
	{
		return size;
	}

	//////////////////////////////////////////////////////////////////////

protected:

	void *		data;
	size_t		size;
};

//////////////////////////////////////////////////////////////////////

struct FileResource: Resource
{
	FileResource(tchar const *name);
	~FileResource();
};

//////////////////////////////////////////////////////////////////////

struct WinResource: Resource
{
	WinResource(DWORD resourceID);
	~WinResource();
};
