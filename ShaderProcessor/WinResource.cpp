//////////////////////////////////////////////////////////////////////

#include "stdafx.h"

//////////////////////////////////////////////////////////////////////

Resource::Resource()
	: data(null)
	, size(0)
{
}

//////////////////////////////////////////////////////////////////////

Resource::~Resource()
{
	assert(data == null);
	assert(size == 0);
}

//////////////////////////////////////////////////////////////////////

WinResource::WinResource(DWORD resourceid)
	: Resource()
{
	LoadResource(resourceid, &data, &size);
}

//////////////////////////////////////////////////////////////////////

WinResource::~WinResource()
{
	if(data != null)
	{
		FreeResource(data);
		data = null;
	}
	size = 0;
}

//////////////////////////////////////////////////////////////////////

FileResource::FileResource(tchar const *filename)
	: Resource()
{
	data = LoadFile(filename, &size);
}

//////////////////////////////////////////////////////////////////////

FileResource::~FileResource()
{
	Delete(data);
	size = 0;
}

